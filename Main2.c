void app_main(void)
{
    // 1. Inicializar NVS (Necesario para Wi-Fi)
    esp_err_t ret = nvs_flash_init();
    if (ret == ESP_ERR_NVS_NO_FREE_PAGES || ret == ESP_ERR_NVS_NEW_VERSION_FOUND) {
        ESP_ERROR_CHECK(nvs_flash_erase());
        ESP_ERROR_CHECK(nvs_flash_init());
    }

    // 2. Inicializar Wi-Fi
    wifi_init_sta();

    // 3. Inicializar I2C y Sensor BMI270
    ESP_LOGI(TAG, "--- Iniciando BMI270 ---");
    ESP_ERROR_CHECK(bmi_init());
    softreset();
    chipid();
    initialization();
    check_initialization();
    bmipowermode();
    internal_status();    
    ESP_LOGI(TAG, "--- BMI270 Listo ---");

    // 4. Crear la tarea de lectura y envío TCP
    xTaskCreatePinnedToCore(
        sensor_tcp_task,    // Función de la tarea
        "sensor_tcp_task",  // Nombre (para depuración)
        4096,               // Tamaño de la pila (Stack size)
        NULL,               // Parámetros de la tarea
        5,                  // Prioridad
        NULL,               // Handle de la tarea (opcional)
        tskNO_AFFINITY      // Correr en cualquier núcleo
    );
    
    ESP_LOGI(TAG, "app_main finalizado, la tarea de sensor/TCP está corriendo.");
}