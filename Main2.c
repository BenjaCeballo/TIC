static void sensor_tcp_task(void *arg)
{
    // Variables de la lógica de lectura
    uint8_t reg_intstatus = 0x03, tmp; // 0x03 = ACC_DRDY, 0x05 = GYR_DRDY
    int bytes_data8 = 12; // 6 bytes Accel + 6 bytes Gyro
    uint8_t reg_data = 0x0C; // Registro de inicio de datos (ACC_X_LSB)
    uint8_t data_data8[bytes_data8];
    uint16_t acc_x, acc_y, acc_z, gyr_x, gyr_y, gyr_z;

    // Buffer para enviar datos
    char tx_buffer[256];

    for (;;) {
        // 1) Asegurar socket conectado (con reintentos)
        int sock = -1;
        while ((sock = connect_tcp()) < 0) {
            vTaskDelay(pdMS_TO_TICKS(2000)); // espera y reintenta
        }

        // 2) Bucle de comunicación (lectura y envío)
        for (;;) {
            
            // 1. Polling para datos listos (Data Ready)
            // El bit 7 (0x80) del registro 0x03 es 'acc_drdy'
            bmi_read(&reg_intstatus, &tmp, 1);
            
            if ((tmp & 0b10000000) == 0x80)
            { 
                // 2. Datos listos, leer los 12 bytes (Acc + Gyr)
                ret= bmi_read( &reg_data, (uint8_t*) data_data8, bytes_data8);
                if(ret != ESP_OK){
                    ESP_LOGW(TAG, "Error lectura BMI: %s \n",esp_err_to_name(ret));
                    continue; // Saltar esta iteración
                }

                // 3. Parsear datos
                acc_x = ((uint16_t) data_data8[1] << 8) | (uint16_t) data_data8[0];
                acc_y = ((uint16_t) data_data8[3] << 8) | (uint16_t) data_data8[2];
                acc_z = ((uint16_t) data_data8[5] << 8) | (uint16_t) data_data8[4];

                gyr_x = ((uint16_t) data_data8[7] << 8) | (uint16_t) data_data8[6];
                gyr_y = ((uint16_t) data_data8[9] << 8) | (uint16_t) data_data8[8];
                gyr_z = ((uint16_t) data_data8[11] << 8) | (uint16_t) data_data8[10];

                // 4. Convertir a floats (usando los factores de tu código)
                // Rango +/-8g (8.000)
                float f_acc_x = (int16_t)acc_x * (8.000 / 32768.0);
                float f_acc_y = (int16_t)acc_y * (8.000 / 32768.0);
                float f_acc_z = (int16_t)acc_z * (8.000 / 32768.0);
                // Rango +/-2000dps (34.90659 rad/s)
                float f_gyr_x = (int16_t)gyr_x * (34.90659 / 32768.0);
                float f_gyr_y = (int16_t)gyr_y * (34.90659 / 32768.0);
                float f_gyr_z = (int16_t)gyr_z * (34.90659 / 32768.0);
                
                // 5. Formatear datos como CSV (fácil de parsear en Python)
                int len = snprintf(tx_buffer, sizeof(tx_buffer), 
                                 "%.4f,%.4f,%.4f,%.4f,%.4f,%.4f\n",
                                 f_acc_x, f_acc_y, f_acc_z,
                                 f_gyr_x, f_gyr_y, f_gyr_z);

                // 6. Enviar datos por TCP
                int sent = send(sock, tx_buffer, len, 0);
                if (sent < 0) {
                    ESP_LOGW(TAG, "Error en send(), cerrando y reintentando...");
                    break; // Rompe el bucle interno para reconectar
                } 
                // ESP_LOGI(TAG, "TX: %s", tx_buffer); // Descomentar para depurar (inundará el log)

            } else {
                // No hay datos listos, ceder CPU
                vTaskDelay(pdMS_TO_TICKS(1)); 
            }
        }

        // 3) Cerrar y reintentar
        if (sock >= 0) {
            ESP_LOGW(TAG, "Cerrando socket.");
            close(sock);
        }
        vTaskDelay(pdMS_TO_TICKS(1000));
    }
}

