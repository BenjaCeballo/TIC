void check_initialization(void){
    uint8_t reg_internalstatus=0x21;
    uint8_t tmp;

    vTaskDelay(pdMS_TO_TICKS(1000)); // Dar tiempo a que inicialice

    bmi_read( &reg_internalstatus, &tmp, 1);
    uint8_t init_status = tmp & 0b00000011; // Solo nos importan los dos primeros bits

    printf("Internal Status (Reg 0x21): 0x%02X\n", tmp);

    if (init_status == 0x01) {
        // Bit 0 (init_ok) = 1. ¡Éxito!
        printf("Comprobación Inicialización: OK (Status: 0x01)\n\n");
    } else if (init_status == 0x02) {
        // Bit 1 (init_err) = 1. Error.
        printf("Inicialización fallida (Status: 0x02 - INIT_ERR)\n");
        printf("El sensor reportó un error al cargar el firmware. Revisa la carga del config_file.\n\n");
        vTaskDelay(portMAX_DELAY); // Detener
    } else if (init_status == 0x00) {
        // (0x00) = init_running
        printf("Inicialización fallida (Status: 0x00 - INIT_RUNNING)\n");
        printf("La inicialización del sensor todavía está en progreso o nunca se completó.\n\n");
        vTaskDelay(portMAX_DELAY); // Detener
    } else {
         // (0x03) = Undefined
        printf("Inicialización fallida (Status: 0x%02X - Estado Indefinido)\n\n", init_status);
        vTaskDelay(portMAX_DELAY); // Detener
    }
}

