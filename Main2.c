esp_err_t bmi_read( uint8_t *data_address, uint8_t *data_rd, size_t size) {
    if (size == 0) {
        return ESP_OK;
    }
    esp_err_t ret;

    // Escribir la dirección del registro que queremos leer
    ret = i2c_master_transmit(device, data_address, 1, pdMS_TO_TICKS(1000));
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "Fallo en I2C transmit (lectura): %s", esp_err_to_name(ret));
        return ret;
    }

    // Leer los datos del registro
    ret = i2c_master_receive(device, data_rd, size, pdMS_TO_TICKS(1000));
     if (ret != ESP_OK) {
        ESP_LOGE(TAG, "Fallo en I2C receive: %s", esp_err_to_name(ret));
    }
    return ret;
}

esp_err_t bmi_write(uint8_t *data_address, uint8_t *data_wr, size_t size) {
    esp_err_t ret;

    // Crear un buffer que contenga [DIRECCIÓN_REGISTRO, DATO1, DATO2, ...]
    uint8_t *full_data = malloc(1 + size);
    if (full_data == NULL) {
        ESP_LOGE(TAG, "Fallo al alocar memoria para buffer I2C");
        return ESP_ERR_NO_MEM;
    }
    
    full_data[0] = *data_address;
    memcpy(&full_data[1], data_wr, size);

    // Realizar la transmisión I2C
    ret = i2c_master_transmit(device, full_data, 1 + size, pdMS_TO_TICKS(1000));
    
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "Fallo en I2C transmit (escritura): %s", esp_err_to_name(ret));
    }
    
    free(full_data);
    return ret;
}

esp_err_t bmi_init() {
    esp_err_t ret;
    ESP_LOGI(TAG, "Inicializando bus I2C...");

    i2c_master_bus_config_t i2c_mst_config = {
        .clk_source = I2C_CLK_SRC_DEFAULT,
        .i2c_port = I2C_NUM_0, // Usar I2C_NUM_0
        .scl_io_num = I2C_MASTER_SCL_IO,
        .sda_io_num = I2C_MASTER_SDA_IO,
        .glitch_ignore_cnt = 7,
        .flags.enable_internal_pullup = true,
    };

    i2c_master_bus_handle_t bus_handle;
    ESP_ERROR_CHECK(i2c_new_master_bus(&i2c_mst_config, &bus_handle));

    i2c_device_config_t dev_cfg = {
        .dev_addr_length = I2C_ADDR_BIT_LEN_7,
        .device_address = ESP_SLAVE_ADDR,
        .scl_speed_hz = I2C_MASTER_FREQ_HZ,
    };

    // Añadir dispositivo I2C
    ESP_LOGI(TAG, "Añadiendo dispositivo BMI270 en 0x%X", ESP_SLAVE_ADDR);
    ret = i2c_master_bus_add_device(bus_handle, &dev_cfg, &device);
    if (ret != ESP_OK) {
        ESP_LOGE(TAG, "Fallo al añadir dispositivo I2C: %s", esp_err_to_name(ret));
    }
    return ret;
}


